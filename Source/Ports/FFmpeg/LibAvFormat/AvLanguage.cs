		/********************************************************************/
		/// <summary>
		/// Convert a language code to a target codespace. The source
		/// codespace is guessed
		/// </summary>
		/********************************************************************/
		public static CPointer<char> FF_Convert_Lang_To(CPointer<char> lang, AvLangCodespace target_Codespace)//XX 741
		{
			CPointer<LangEntry> entry = null;
			c_int nb_Codespaces = (c_int)Macros.FF_Array_Elems(lang_Table_Counts);

			if ((c_int)target_Codespace >= nb_Codespaces)
				return null;

			CPointer<LangEntry> langTable = lang_Table;

			for (c_int i = 0; (entry == null) && (i < nb_Codespaces); i++)
				entry = CArray.bsearch(lang, langTable + lang_Table_Offsets[i], lang_Table_Counts[i], Lang_Table_Compare);

			if (entry == null)
				return null;

			for (c_int i = 0; i < nb_Codespaces; i++)
			{
				if ((entry >= (langTable + lang_Table_Offsets[(c_int)target_Codespace])) && (entry < (langTable + lang_Table_Offsets[(c_int)target_Codespace] + lang_Table_Counts[(c_int)target_Codespace])))
					return entry[0].Str;
				else
					entry = langTable + entry[0].Next_Equivalent;
			}

			if (target_Codespace == AvLangCodespace.Iso639_2_Term)
				return FF_Convert_Lang_To(lang, AvLangCodespace.Iso639_2_Bibl);

			return null;
		}

